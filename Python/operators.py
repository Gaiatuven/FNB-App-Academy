# Operators

# Plus operator
# minus operator
# multiply operator
# divide operator
# Exponent operator

x = 10
y = 5

# Plus operator
print(f"Plus: {x + y}")  # Output: 15
# Minus operator
print(f"Minus: {x - y}")  # Output: 5
# Multiply operator
print(f"Multiply: {x * y}")  # Output: 50
# Divide operator
print(f"Divide: {x / y}")  # Output: 2.0
# Exponent operator
print(f"Exponent: {x ** y}")  # Output: 100000
# Modulus operator
print(f"Modulus: {x % y}")  # Output: 0
# Floor division operator
print(f"Floor Division: {x // y}")  # Output: 2


# Operators with strings

string1 = "Hello"
string2 = "World"

# Concatenation operator
print(f"Concatenation: {string1 + ' ' + string2}")  # Output: Hello World
# Repetition operator
print(f"Repetition: {string1 * 3}")  # Output: HelloHello
# Length operator
print(f"Length of string1: {len(string1)}")  # Output: 5
print(f"Length of string2: {len(string2)}")  # Output: 5